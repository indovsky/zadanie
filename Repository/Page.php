class Page {

    const DEFAULT_ARGS = [
        'post_type' =>  'page',
        'orderby'   =>  'menu_order',
        'order' =>  'DESC',
    ];

    public function __construct( array $args = [] )
    {
        $this->args = array_merge(self::DEFAULT_ARGS, $args);
    }

    /**
     * @param int $parent_id Page id
     */
    public function get_by_parent( int $parent_id ): array
    {
        $this->args['post_parent'] = $parent_id;

        $child_pages = new WP_Query($this->args);
        return $child_pages->posts;
    }
}