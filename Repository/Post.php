class Post {

    const DEFAULT_ARGS = [
        'orderby'   =>  'ID',
        'order' =>  'DESC',
        'post_type' =>  'post',
        'post_status' => 'publish',
        'meta_query'    =>  [
            'key'   =>  '_thumbnail_id',
            'compare'   =>  'EXISTS'
        ]
    ];

    public function __construct( array $args = [] )
    {
        $this->args = array_merge(self::DEFAULT_ARGS, $args);
    }

    /**
     * @param int $count Posts count
     */
    public function get_latest_posts( int $count ): array
    {
        $this->args['posts_per_page'] = $count;
        $latest_posts = new WP_Query($this->args);

        return $latest_posts->posts;
    }
}