class Post {
    private $post;

    public function __construct( WP_Post $post )
    {
        $this->post = $post;
        $this->ID = $this->post->ID;
    }

    /**
     * Get WP_Post object methods
     */
    public function get()
    {
        return $this->post;
    }

    /**
     * Get post thumbnail attributes
     */
    public function get_thumbnail(): array
    {
        $thumbnail_id = get_post_thumbnail_id( $this->ID );
        $alt = get_post_meta($thumbnail_id, '_wp_attachment_image_alt', true);

        return [
            'src'   =>  get_the_post_thumbnail_url( $this->ID, 'full' ),
            'alt'   =>  $alt
        ];
    }

    /**
     * Get post url
     */
    public function get_url(): ?string
    {
        return esc_url( get_permalink( $this->ID ) );
    }
}